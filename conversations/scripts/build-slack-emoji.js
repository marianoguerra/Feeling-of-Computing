#!/usr/bin/env node
// Generates src/slack-emoji-data.js with Slack-specific emoji aliases
// not present in iamcal/emoji-data.
// Add new aliases to SLACK_ALIASES as you encounter them.

import { writeFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUTPUT = join(__dirname, "..", "src", "slack-emoji-data.js");

// Slack emoji names that aren't in iamcal/emoji-data, mapped to unified hex codes.
// Add more as needed when you see unresolved :name: in the viewer.
const SLACK_ALIASES = {
  "thank-you": "1F64F",
  "simple_smile": "1F642",
  "thumbsup_all": "1F44D",
  "white_frowning_face": "2639-FE0F",
  "lightning": "26A1",
  "telephone": "260E-FE0F",
};

const output = `// Generated by scripts/build-slack-emoji.js
// Slack-specific emoji aliases not in iamcal/emoji-data
// Do not edit manually — run: node scripts/build-slack-emoji.js

const slackAliases = ${JSON.stringify(SLACK_ALIASES)};

export { slackAliases };
`;

writeFileSync(OUTPUT, output);
console.log(
  `Written ${Object.keys(SLACK_ALIASES).length} Slack aliases to ${OUTPUT}`,
);
